a = 10
b = 3

# Arithmetic Operators
print("Addition:", a + b)       # 13
print("Subtraction:", a - b)      # 7
print("Multiplication:", a * b)    # 30
print("Division:", a / b)         # 3.3333333333333335
print("Modulus:", a % b)        # 1

# Assignment Operators
x = 5
x += 2
print("After += :", x)             # 7

# Comparison Operators
print("a == b:", a == b)           # False
print("a > b:", a > b)            # True

# Logical Operators
print("(a > 5 and b < 5):", a > 5 and b < 5)  # True

# Increment (Python way)
a += 1
print("After increment:", a)     # 11
